CREATE View [dbo].[W_Lj_Sintegra_Reg_61R] As 
    --08/11/2019 - CARLOS G. GONÇALVES - MODASP-8108 - @001 - ALTERADO FILTROS DE MODELO PARA GERACAO REGISTROS 61
	SELECT 
		A.CODIGO_FILIAL,
		A.EMISSAO,
		B.CODIGO_ITEM, 
		MES= MONTH(A.EMISSAO),       
		ANO= YEAR(A.EMISSAO),
		B.QTDE_ITEM,
		B.VALOR_ITEM,
		C.BASE_IMPOSTO,
		C.TAXA_IMPOSTO
	FROM 
		LOJA_NOTA_FISCAL A 
		LEFT JOIN LOJA_NOTA_FISCAL_ITEM B ON A.CODIGO_FILIAL = B.CODIGO_FILIAL AND A.NF_NUMERO = B.NF_NUMERO AND A.SERIE_NF = B.SERIE_NF 
		LEFT JOIN LOJA_NOTA_FISCAL_IMPOSTO C ON B.CODIGO_FILIAL = C.CODIGO_FILIAL AND B.NF_NUMERO = C.NF_NUMERO AND B.SERIE_NF = C.SERIE_NF AND B.ITEM_IMPRESSAO = C.ITEM_IMPRESSAO AND B.SUB_ITEM_TAMANHO = C.SUB_ITEM_TAMANHO AND C.ID_IMPOSTO = 1 
		LEFT JOIN SERIES_NF D ON A.SERIE_NF = D.SERIE_NF 
		LEFT JOIN CTB_ESPECIE_SERIE E ON D.ESPECIE_SERIE = E.ESPECIE_SERIE 
	WHERE 
		--E.NUMERO_MODELO_FISCAL IN ('2', '02', '2D') 
        E.NUMERO_MODELO_FISCAL IN ('2', '02', '2D', '65', '63') -- @001 



