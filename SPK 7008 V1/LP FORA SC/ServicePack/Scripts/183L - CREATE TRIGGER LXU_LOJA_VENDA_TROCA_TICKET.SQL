CREATE trigger [dbo].[LXU_LOJA_VENDA_TROCA_TICKET] on [dbo].[LOJA_VENDA_TROCA_TICKET] for UPDATE NOT FOR REPLICATION 
as  

BEGIN
	UPDATE 	LOJA_VENDA_TROCA_TICKET 
	SET 	DATA_PARA_TRANSFERENCIA = Deleted.DATA_PARA_TRANSFERENCIA
	FROM 	LOJA_VENDA_TROCA_TICKET, INSERTED, Deleted
	WHERE 	LOJA_VENDA_TROCA_TICKET.TICKET        = INSERTED.TICKET        
	AND     LOJA_VENDA_TROCA_TICKET.CODIGO_FILIAL = INSERTED.CODIGO_FILIAL 
	AND     LOJA_VENDA_TROCA_TICKET.ITEM          = INSERTED.ITEM         
	AND     LOJA_VENDA_TROCA_TICKET.DATA_VENDA    = INSERTED.DATA_VENDA    
	AND		LOJA_VENDA_TROCA_TICKET.DATA_VENDA    = Deleted.DATA_VENDA     
	AND		LOJA_VENDA_TROCA_TICKET.ITEM          = Deleted.ITEM           
	AND		LOJA_VENDA_TROCA_TICKET.CODIGO_FILIAL = Deleted.CODIGO_FILIAL  
	AND		LOJA_VENDA_TROCA_TICKET.TICKET        = Deleted.TICKET


END