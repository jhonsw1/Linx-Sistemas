					AND [COLUMN_NAME] = 'NUMERO_TITULO'
					AND CHARACTER_MAXIMUM_LENGTH < 36)
BEGIN 
	ALTER TABLE [DBO].[LOJA_VENDA_PARCELAS] ALTER COLUMN NUMERO_TITULO VARCHAR(36) 
END
